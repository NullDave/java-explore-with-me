CREATE TABLE IF NOT EXISTS users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name VARCHAR(250) NOT NULL,
  email VARCHAR(254) NOT NULL,
  CONSTRAINT pk_user PRIMARY KEY (id),
  CONSTRAINT UQ_USER_EMAIL UNIQUE (email)
);

CREATE TABLE IF NOT EXISTS locations (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  lat FLOAT NOT NULL,
  lon FLOAT NOT NULL,
  CONSTRAINT pk_location PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS categories (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name VARCHAR(50) NOT NULL,
  CONSTRAINT pk_category PRIMARY KEY (id),
  CONSTRAINT uq_name UNIQUE (name)
);

CREATE TABLE IF NOT EXISTS events (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  title VARCHAR(120) NOT NULL,
  annotation VARCHAR(2000) NOT NULL,
  description VARCHAR(7000) NOT NULL,
  event_date TIMESTAMP WITHOUT TIME ZONE NOT NULL,
  paid BOOLEAN NOT NULL,
  confirmed_requests BIGINT,
  participant_limit INTEGER NOT NULL,
  request_moderation BOOLEAN NOT NULL,
  initiator_id BIGINT NOT NULL,
  category_id BIGINT NOT NULL,
  location_id BIGINT NOT NULL,
  published_on TIMESTAMP WITHOUT TIME ZONE,
  created_on TIMESTAMP WITHOUT TIME ZONE,
  state VARCHAR(15),
  views BIGINT,
  CONSTRAINT pk_event PRIMARY KEY (id),
  CONSTRAINT fk_initiator_id FOREIGN KEY (initiator_id) REFERENCES users(id) ON DELETE CASCADE,
  CONSTRAINT fk_category_id FOREIGN KEY (category_id) REFERENCES categories(id),
  CONSTRAINT fk_location_id FOREIGN KEY (location_id) REFERENCES locations(id)
);

create TABLE IF NOT EXISTS comments (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  event_id BIGINT NOT NULL,
  user_id BIGINT NOT NULL,
  text VARCHAR(2000) NOT NULL,
  is_delete_by_user BOOLEAN NOT NULL,
  modified TIMESTAMP WITHOUT TIME ZONE,
  created TIMESTAMP WITHOUT TIME ZONE NOT NULL,
  CONSTRAINT pk_comment PRIMARY KEY (id),
  CONSTRAINT fk_comments_user_id FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
  CONSTRAINT fk_comments_event_id FOREIGN KEY (event_id) REFERENCES events(id) ON DELETE CASCADE
);

create TABLE IF NOT EXISTS requests (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  requester_id BIGINT NOT NULL,
  event_id BIGINT NOT NULL,
  status VARCHAR(15) NOT NULL,
  created TIMESTAMP WITHOUT TIME ZONE NOT NULL,
  CONSTRAINT pk_request PRIMARY KEY (id),
  CONSTRAINT fk_requester_id FOREIGN KEY (requester_id) REFERENCES users(id) ON DELETE CASCADE,
  CONSTRAINT fk_event_id FOREIGN KEY (event_id) REFERENCES events(id) ON DELETE CASCADE,
  CONSTRAINT uq_requester_event UNIQUE(requester_id, event_id)
);

create TABLE IF NOT EXISTS compilations (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  title VARCHAR(50) NOT NULL,
  pinned BOOLEAN NOT NULL,
  CONSTRAINT pk_compilation PRIMARY KEY (id)
);

create TABLE IF NOT EXISTS compilations_events (
  compilation_id BIGINT NOT NULL,
  event_id BIGINT NOT NULL,
  CONSTRAINT fk_compilation_id FOREIGN KEY (compilation_id) REFERENCES compilations(id) ON DELETE CASCADE,
  CONSTRAINT fk_compilation_event_id FOREIGN KEY (event_id) REFERENCES events(id) ON DELETE CASCADE
);